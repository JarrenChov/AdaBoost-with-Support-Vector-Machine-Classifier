# Check if type is of boolean
def is_bool(value):
  if isinstance(value, bool):
    return True
  return False


# Check if type is of float
def is_float(value):
  if isinstance(value, float):
    return True
  return False


# Check if type is of integer
def is_int(value):
  if isinstance(value, int):
    return True
  return False


# Check if type is of list
def is_list(value):
  if isinstance(value, list):
    return True
  return False


# Check if type is of string
def is_str(value):
  if isinstance(value, str):
    return True
  return False
